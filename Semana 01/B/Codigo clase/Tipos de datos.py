# Enteros - int
x = 45
print(x)
print(type(45))
# binario
x = 0b10
print(x)
# octal
x = 0o10
print(x)
# hexadecimal
x = 0x10
print(x)
print(type(x))

# Flotantes - float
y = 10.492749823792949
print(y)
print(type(y))

print(type(0))
print(type(0.0))



# Booleanos - bool
# 1 = True , 0 = False
z = False
print(z)
print(type(z))

# String - str
cadena = ""
cadena_2 = 'Jorge Villavicencio'
print(cadena_2)
print(type(cadena_2))


